    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {

        $request->validate([
            'title' => 'required|min:3|max:181',
            'description' => 'required|min:3|max:300',
            'category' => 'required|exists:categories,id'
        ], [
            'title.max' => 'عربي',
            'title.min' => 'عربي 1'
        ]);
        //  dd(['title' => $request->title, 'description' => 'description']);
        // dd($request->only('description', 'title'));
        //Product::create(array_merge($request->only('description', 'title'), ['category_id' => $request->category]));
        $product = new Product($request->only('title', 'description'));
        $category = Category::find($request->category);
        $product->category()->associate($category);
        $product->save();

        return redirect()->route('products.index');
    }

